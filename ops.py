# math_operations.py


def add_numbers(x: float, y: float) -> float:
    """
    Adds two numbers.

    Parameters:
        x (float): The first number.
        y (float): The second number.

    Returns:
        float: The sum of x and y.
    """
    return x + y


def substract_numbers(x: float, y: float) -> float:
    """
    Subtracts one number from another.

    Parameters:
        x (float): The number to be subtracted from.
        y (float): The number to subtract.

    Returns:
        float: The result of subtracting y from x.
    """
    return x - y


def square_number(x: float) -> float:
    """
    Squares numbers

    Parameters:
        x (float): The number to be squared.

    Returns:
        float: The result of the square.
    """
    return x**2
